#include "eventlib.h"

void	set_rainbow(char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 1;
	while (str[i])
	{
		printf("\033[0;3%dm%c", k, str[i]);
		k++;
		i++;
		if (k == 7)
			k = 1;
	}
	printf("\n");
}
